package phase1.modifiers.p1;

public class ClassM {
	
	private int i = 5;
	long j = 40;
	protected float k = 15;
	
	public void function1() {
		System.out.println("This is a public function in class m");
	}
	protected void function2() {
		System.out.println("This is a protected function in class m");
	}
	void function3() {
		System.out.println("This is a default function in class m");
	}
	private void function4() {
		System.out.println("This is a private function in class m");
	}

}
